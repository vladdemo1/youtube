"""This module contains main info about indexes in Python"""

numbers = (100, 200, 300, 400, 500, 600, 700, 800, 900, 1000)

# start:finish:step

print('\nnumbers     -> ', numbers)
print('numbers[:]  -> ', numbers[:])
print('numbers[::] -> ', numbers[::])

print('\nnumbers[3:] -> ', numbers[3:])

print('\nnumbers[:3] -> ', numbers[:3])

print('\nnumbers [3::2] -> ', numbers[3::2])

print('\nnumbers[:8:3] -> ', numbers[:8:3])

print('\nnumbers[::-1] -> ', numbers[::-1])

print('\nnumbers[-1] -> ', numbers[-1])
